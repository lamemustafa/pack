import type { FiledReturnsDownloadScope, PortalFlowStepResult } from "../core/contracts";
import {
  filedReturnsConcreteArtifactLabel,
  type FiledReturnsConcreteArtifactType,
} from "../core/filed-returns-artifacts";
import { isUnconfirmedBrowserDownloadSignal } from "./download-evidence-signals";

export function withDownloadedArtifactSignal(
  flowStep: PortalFlowStepResult,
  artifactType: FiledReturnsConcreteArtifactType,
): PortalFlowStepResult {
  if (flowStep.state !== "downloaded") return flowStep;
  return {
    ...flowStep,
    safeSignals: [
      ...flowStep.safeSignals,
      ...(flowStep.safeSignals.includes(`filed-return-artifact-downloaded:${artifactType}`)
        ? []
        : [`filed-return-artifact-downloaded:${artifactType}`]),
    ],
  };
}

export function withArtifactDownloadMessage(
  flowStep: PortalFlowStepResult,
  scope: FiledReturnsDownloadScope,
  artifactType: FiledReturnsConcreteArtifactType,
): PortalFlowStepResult {
  if (flowStep.state !== "downloaded") {
    return withUnconfirmedArtifactSignal(flowStep, scope, artifactType);
  }
  const artifactLabel = filedReturnsConcreteArtifactLabel(artifactType);
  return {
    ...flowStep,
    safeMessage: `The browser reported that the filed-return ${artifactLabel} download completed. Check the local downloads folder for the GST Portal file.`,
  };
}

export function targetReviewScope(
  scope: FiledReturnsDownloadScope,
  artifactType: FiledReturnsConcreteArtifactType,
): FiledReturnsDownloadScope {
  if (artifactType === "PDF") {
    const pdfScope = { ...scope };
    delete pdfScope.artifactType;
    return pdfScope;
  }
  return { ...scope, artifactType };
}

function withUnconfirmedArtifactSignal(
  flowStep: PortalFlowStepResult,
  scope: FiledReturnsDownloadScope,
  artifactType: FiledReturnsConcreteArtifactType,
): PortalFlowStepResult {
  if (flowStep.state !== "download-unconfirmed" && flowStep.state !== "blocked") return flowStep;
  if (!flowStep.safeSignals.some(isUnconfirmedBrowserDownloadSignal)) {
    return flowStep;
  }

  const artifactLabel = filedReturnsConcreteArtifactLabel(artifactType);
  const isGstr2bPortalBlobClick = flowStep.safeSignals.includes(
    "gstr2b-portal-blob-download-clicked",
  );
  return {
    ...flowStep,
    safeSignals: [
      ...flowStep.safeSignals,
      ...(flowStep.safeSignals.includes(`filed-return-artifact-unconfirmed:${artifactType}`)
        ? []
        : [`filed-return-artifact-unconfirmed:${artifactType}`]),
      ...(isGstr2bPortalBlobClick &&
      !flowStep.safeSignals.includes("browser-download-save-dialog-may-be-open")
        ? ["browser-download-save-dialog-may-be-open"]
        : []),
    ],
    safeMessage: unconfirmedArtifactMessage(scope, artifactType, artifactLabel),
  };
}

function unconfirmedArtifactMessage(
  scope: FiledReturnsDownloadScope,
  artifactType: FiledReturnsConcreteArtifactType,
  artifactLabel: string,
): string {
  if (scope.returnType === "GSTR-2B") {
    return `Pack clicked the GSTR-2B ${artifactLabel} portal download control, but the browser did not report a completed download. Because GSTR-2B is generated by the portal page as a browser blob download, Brave's ask-where-to-save dialog can still block completion; press Save in the dialog or turn off that browser setting, then retry.`;
  }

  if (artifactType === "EXCEL") {
    return "Pack clicked the filed GSTR-1 e-invoice details Excel download control, but the browser did not report an Excel download. If Brave blocked multiple downloads, allow downloads for the GST Portal; if the portal shows no e-invoice file, retry after the file is available.";
  }

  return `Pack clicked the filed-return ${artifactLabel} download control, but the browser did not report a completed download. Allow downloads for the GST Portal, then retry.`;
}
